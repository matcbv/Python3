# Possuímos diversos métodos de verificação para strings. Alguns desses são:

# isupper(): Verifica se determinada string está em maiúscula.

# islower() - Verifica se a string está em minúsculo.

# isalnum(): Verifica se a string está em alfanumérico (números ou/e letras).

# isalpha(): Verifica se a string é alfabética.

# isdigit() - Verifica se todos os caracteres dentro da string são dígitos, incluindo dígitos do sistema decimal,
# superscripts e subscripts.

# isdecimal() - Verifica se todos os caracteres dentro da string são dígitos do sistema decimal, excluindo caracteres
# que representam números em outras línguas e caracteres especiais como superscripts e subscripts.

# isnumeric() - Verifica se todos os caracteres dentro da string são numéricos, incluindo caracteres que representam
# números em outras línguas, dígitos do sistema decimal, superscripts e subscripts.

# isspace() - Verifica se há apenas caracteres de espaço na string.

# istitle() - Verifica se a sequência for uma sequência com título.

# isprintable() - Verifica se todos os caracteres da string forem imprimíveis ou se a string estiver vazia.

# isidentifier() - Verifica se a string for um identificador válido no Python.

# isinstance(objeto, classe) - Verifica se o valor é uma instância de determinada classe.
# Podemos passar mais de uma classe para ser verificada. Ex.: isinstance(objeto, (int, float)).
