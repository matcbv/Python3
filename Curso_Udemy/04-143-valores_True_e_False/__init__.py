# Aqui veremos quais valores retornam False para cada tipo de 05-198-classes em Python.
lista = []  # Uma lista vazia retorna False.
dicionario = {}  # Um dicionário vazio retorna False.
conjunto = set()  # Um conjunto vazio retorna False.
tupla = ()  # Uma tupla vazia retorna False.
inteiro = 0  # Um inteiro de valor 0 retorna False.
flutuante = 0.0  # Um flutuante de valor 0 também retorna False.
var = None  # Um objeto de valor None retorna False.
var_falsa = False  # Um objeto de valor False, obviamente retorna False.
intervalo = range(0)  # Um intervalo que não possui range retorna False.
