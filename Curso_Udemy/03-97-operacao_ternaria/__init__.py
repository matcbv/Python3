# Aqui verificamos se o valor 1 é igual a 2, o que retorna False.
valor = 1 == 2
# Aqui realizamos uma operação ternária. Uma operação ternário é um if e else de uma só linha.
print('São iguais' if valor else 'São diferentes')

# Realizamos uma operação ternária em casos simples, onde não são envolvidos tantos valores e nem
